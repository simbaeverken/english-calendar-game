function updateLanguage() {
  document.getElementById("title").innerText = translations[currentLanguage].title;
  document.getElementById("infoContent").innerText = translations[currentLanguage].info;
}

function showSection(sectionId) {
  document.querySelectorAll(".section").forEach(el => el.style.display = "none");
  document.getElementById(sectionId).style.display = "block";
}

function startGame() {
  questionIndex = 0;
  attempts = 3;
  loadQuestion();
}

function loadQuestion() {
  clearTimeout(timer);
  const q = questions[questionIndex];
  document.getElementById("question").innerText = q.q[currentLanguage];
  const answersDiv = document.getElementById("answers");
  answersDiv.innerHTML = "";
  q.answers.forEach((a, i) => {
    const btn = document.createElement("button");
    btn.innerText = a;
    btn.onclick = () => checkAnswer(i);
    btn.onmousedown = () => document.getElementById("clickSound").play();
    answersDiv.appendChild(btn);
  });
  document.getElementById("attempts").innerText = `Attempts: ${attempts}`;
  let timeLeft = 30;
  document.getElementById("timer").innerText = `Time: ${timeLeft}s`;
  timer = setInterval(() => {
    timeLeft--;
    document.getElementById("timer").innerText = `Time: ${timeLeft}s`;
    if (timeLeft === 0) {
      clearInterval(timer);
      wrong();
    }
  }, 1000);
}

function checkAnswer(index) {
  const q = questions[questionIndex];
  if (index === q.correct) {
    document.getElementById("correctSound").play();
    questionIndex++;
    if (questionIndex < questions.length) {
      loadQuestion();
    } else {
      alert("You completed all levels!");
    }
  } else {
    wrong();
  }
}

function wrong() {
  document.getElementById("wrongSound").play();
  attempts--;
  if (attempts <= 0) {
    alert("Game Over!");
  } else {
    loadQuestion();
  }
}

